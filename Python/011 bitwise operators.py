print(~12)
#output is -13

print(12 & 13)
# the output is 12
# 1100 & 1101 = 1100 = 12

print(12 | 13)
# the output is 13
# 1100 | 1101 = 1101 = 13

print(12^13)
#the answer is 1
# 1100 ^ 1101 = 0001 = 1

print(10<<2)
#answer will be 40
# discard leftmost 2 bits and apply 2 0s at the right 

print(10>>2)
#answer will be 2
# discard rightmos 2 bits and apply 2 0s at the left